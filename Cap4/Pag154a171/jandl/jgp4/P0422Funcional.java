package jandl.jgp4;
/* Interfaces funcionais servem para declaração de apenas um método abstrato. A realização (ou 'implementação', que serve melhor para se referir à métodos de interfaces, as quais são realizadas pelas classes) destas interfaces exige apenas a implementação desse método abstrato. */

@FunctionalInterface /* Indica que deverá conter apenas um método */
public interface P0422Funcional {
	void sort(); /* Método (abstrato) funcional, declarado implicitamente público */
}
